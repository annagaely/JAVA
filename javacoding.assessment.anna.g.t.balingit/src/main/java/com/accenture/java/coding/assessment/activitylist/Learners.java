package com.accenture.java.coding.assessment.activitylist;

public class Learners {


    private String name;
    private int hours;
    public Learners(String name, int hours) {
        this.name = name;
        this.hours = hours;
    }
    public String getName() {
        return name;
    }
    public int getHours() {
        return hours;
    }
}
